        public static SiteTypeDef? GetSiteType(string buildType)
        {
            if (string.IsNullOrEmpty(buildType)) return null;
            var cleaned = buildType.Replace(" (primary)", "");
            return SiteTypes.Find(st =>
            {
                if (Array.IndexOf(st.SubTypes, cleaned) >= 0) return true;
                if (st.AltTypes != null && Array.IndexOf(st.AltTypes, cleaned) >= 0) return true;
                if (cleaned.Length > 0)
                {
                    var trimmed = cleaned[..^1];
                    if (Array.IndexOf(st.SubTypes, trimmed) >= 0) return true;
                    if (st.AltTypes != null && Array.IndexOf(st.AltTypes, trimmed) >= 0) return true;
                }
                return false;
            });
        }

        public static List<CargoCost> GetColonizationCosts(string displayName2)
        {
            return ColonizationCosts.TryGetValue(displayName2, out var costs) ? costs : new List<CargoCost>();
        }

        public static int GetTotalHaul(string displayName2)
        {
            var costs = GetColonizationCosts(displayName2);
            return costs.Sum(c => c.Count);
        }
