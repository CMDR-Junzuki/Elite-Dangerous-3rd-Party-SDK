export function getSiteType(buildType: string): SiteType | undefined {
  if (!buildType) return undefined;
  const cleaned = buildType.replace(" (primary)", "");
  return siteTypes.find(
    (st) =>
      st.subTypes.includes(cleaned) ||
      st.altTypes?.includes(cleaned) ||
      st.subTypes.includes(cleaned.slice(0, -1)) ||
      st.altTypes?.includes(cleaned.slice(0, -1)),
  );
}

